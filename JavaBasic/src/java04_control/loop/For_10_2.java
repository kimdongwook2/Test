package java04_control.loop;

public class For_10_2 {
	public static void main(String[] args) {
		
		for(int num = 1; num <= 9; num++) {
			
			for(int dan = 2; dan <= 9; dan++) {
				System.out.print(dan +"x" + num + "=" + (dan * num) + "\t");
			}
			
			System.out.println("");
		}
		
	}// main method end
}// For_10_1 class end

